#include<bits/stdc++.h>
using namespace std;
int main(){
    int t;
    cin>>t;
    while(t--){
        int T,x,y;
        cin>>T>>x>>y;
        int ans;
        if(x<y){
            if(T%x==0){
                ans=T/x;
            }
            else{
                ans=T/x+1;
            }
        }
        else{
            if(T%y==0){
                ans=T/y;
            }
            else{
                ans=T/y+1;
            }
        }
        cout<<ans<<endl;
    }
}
